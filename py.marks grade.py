marks = float(input("enter your marks in computer science:"))
if marks >90:
    print("grade:s")
elif marks >=80 and marks <90:
    print("grade: a")
elif marks >=70 and marks <80:
    print("grade: b")
elif marks >=60 and marks <70:
    print("grade: c")
elif marks >=50 and marks <60:
    print("grade: d")
else :
    print("grade: f")
